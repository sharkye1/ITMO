from Informatics_Lab3_Task1 import find_VT_ITMO


TEST_CASES = [
    ("ВТ слово ИТМО", True),  # простой случай, 1 слово между
    ("ВТ один два три четыре ИТМО", True),  # 4 слова между (разрешено)
    ("ВТ один два три четыре пять ИТМО", False),  # 5 слов между (уже много)
    ("ВТ, слово1; слово2 ИТМО", True),  # разделители пунктуации
    ("ВТ\nслово1 слово2 ИТМО", True),  # перевод строки как разделитель
    ("привет ВТ слово ИТМО мир", True),  # ВТ и ИТМО внутри предложения
    ("ВТИТМО", False),  # нет границ слов
    ("вт слово ИТМО", False),  # строчные 'вт' не должны совпадать
    ("ВТ-слово ИТМО", False),  # 'ВТ-слово' — один токен, не отдельное слово 'ВТ'
    ("ВТ!!! слово ИТМО", True),  # несколько не-словных символов подряд
    ("ВТ ИТМО", True),  # никаких слов между (0 слов)
    ("ВТ  ИТМО", True),  # двойной пробел
    ("ВТ\tслово ИТМО", True),  # таб как разделитель
    ("ВТ.слово.слово ИТМО", True),  # точки разделяют слова
    ("ВТ слово1 слово2 слово3 слово4 слово5 ИТМО", False),  # 5 слов между -> не подходит
    ("ВТ123 слово ИТМО", False),  # 'ВТ123' не равно слову 'ВТ'
    ("123ВТ слово ИТМО", False),  # цифры в начале токена -> не совпадает
    ("ВТ-слово1-слово2 ИТМО", True),  # дефисы дают несколько слов между
    ("ВТ слово, слово; слово: ИТМО", True),  # разная пунктуация
    ("ВТ слово1\nслово2\nслово3\nслово4 ИТМО", True),  # несколько переводов строки
    ("ВТ слово1.word2 ИТМО", True),  # точка как разделитель между словами
    ("ВТ ИТМО и ещё текст", True),  # после ИТМО есть текст
    ("перед ВТ ИТМО", True),  # перед ВТ есть текст
    ("ВТ!1 ИТМО", True),  # сочетание знака и цифры
]


def run_tests():
    ln = len(TEST_CASES)
    passed = 0
    print("Всего тестов:", ln)
    print("-")

    for idx, (test_text, expected) in enumerate(TEST_CASES, 1):
        matches = find_VT_ITMO(test_text)
        found = bool(matches)

        if found == expected:
            print(f"Тест {idx}: пройден")
            passed += 1
        else:
            print(f"Тест {idx}: НЕ ПРОЙДЕН")

        print("  Текст:", test_text)
        print("  Ожидалось:", expected, " Найдено:", found)
        if matches:
            print("  Матчи:", matches)
        print("-----")

    print("Итого пройдено:", passed, "из", ln)


if __name__ == '__main__':
    run_tests()
